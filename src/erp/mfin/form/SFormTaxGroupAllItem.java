    @Override
    public void setRegistry(erp.lib.data.SDataRegistry registry) {
        moTaxGroupAllItem = (SDataTaxGroupAllItem) registry;

        moFieldPkItem.setFieldValue(new int[] { moTaxGroupAllItem.getPkItemId() });

        for (int i = 0; i < moTaxGroupAllItem.getDbmsTaxGroupItems().size(); i++) {
            moPaneAllItem.addTableRow(new SDataTaxGroupItemRow(moTaxGroupAllItem.getDbmsTaxGroupItems().get(i)));
        }
        moPaneAllItem.renderTableRows();
        moPaneAllItem.setTableRowSelection(0);

        jcbPkItemId.setEnabled(false);
        jbPkItemId.setEnabled(false);
    }

    @Override
    public erp.lib.data.SDataRegistry getRegistry() {
        if (moTaxGroupAllItem == null) {
            moTaxGroupAllItem = new SDataTaxGroupAllItem();
            moTaxGroupAllItem.setPkItemId(moFieldPkItem.getKeyAsIntArray()[0]);
            moTaxGroupAllItem.setFkUserNewId(miClient.getSession().getUser().getPkUserId());
        }
        else {
            moTaxGroupAllItem.setFkUserNewId(miClient.getSession().getUser().getPkUserId());     // new tax regions and tax groups will need this
            moTaxGroupAllItem.setFkUserEditId(miClient.getSession().getUser().getPkUserId());
        }

        moTaxGroupAllItem.getDbmsTaxGroupItems().clear();
        for (int i = 0; i < moPaneAllItem.getTableGuiRowCount(); i++) {
            moTaxGroupAllItem.getDbmsTaxGroupItems().add((SDataTaxGroupItem) ((SDataTaxGroupItemRow) moPaneAllItem.getTableRow(i)).getData());
        }

        return moTaxGroupAllItem;
    }

