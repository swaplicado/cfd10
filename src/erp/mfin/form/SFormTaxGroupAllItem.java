    private void windowActivated() {
        if (mbFirstTime) {
            mbFirstTime = false;
            if (jcbPkItemId.isEnabled()) jcbPkItemId.requestFocus(); else jbCancel.requestFocus();
        }
    }

    private void actionPkItemId() {
        miClient.pickOption(SDataConstants.ITMX_ITEM_IOG, moFieldPkItem, null);
    }

    private void actionNewTaxRegion() {
        moFormTaxGroupItem.formReset();
        moFormTaxGroupItem.setValue(SDataConstants.ITMU_ITEM, moFieldPkItem.getKeyAsIntArray());
        moFormTaxGroupItem.setFormVisible(true);

        if (moFormTaxGroupItem.getFormResult() == SLibConstants.FORM_RESULT_OK) {
            moPaneAllItem.addTableRow(new SDataTaxGroupItemRow((SDataTaxGroupItem) moFormTaxGroupItem.getRegistry()));
            moPaneAllItem.renderTableRows();
            moPaneAllItem.setTableRowSelection(moPaneAllItem.getTableGuiRowCount() - 1);
        }
    }

    private void actionEditTaxRegion() {
        int index = moPaneAllItem.getTable().getSelectedRow();

        if (index != -1) {
            moFormTaxGroupItem.formReset();
            moFormTaxGroupItem.setValue(SDataConstants.ITMU_ITEM, moFieldPkItem.getKeyAsIntArray());
            moFormTaxGroupItem.setRegistry((SDataTaxGroupItem) ((SDataTaxGroupItemRow) moPaneAllItem.getSelectedTableRow()).getData());
            moFormTaxGroupItem.setFormVisible(true);

            if (moFormTaxGroupItem.getFormResult() == SLibConstants.FORM_RESULT_OK) {
                moPaneAllItem.setTableRow(new SDataTaxGroupItemRow((SDataTaxGroupItem) moFormTaxGroupItem.getRegistry()), index);
                moPaneAllItem.renderTableRows();
                moPaneAllItem.setTableRowSelection(index);
            }
        }
    }

    private void actionOk() {
        SFormValidation validation = formValidate();

        if (validation.getIsError()) {
            if (validation.getComponent() != null) {
                validation.getComponent().requestFocus();
            }
            if (validation.getMessage().length() > 0) {
                miClient.showMsgBoxWarning(validation.getMessage());
            }
        }
        else {
            mnFormResult = SLibConstants.FORM_RESULT_OK;
            setVisible(false);
        }
    }

    private void actionCancel() {
        mnFormResult = SLibConstants.FORM_RESULT_CANCEL;
        setVisible(false);
    }

    public void linkActionEditTaxRegion() {
        actionEditTaxRegion();
    }

