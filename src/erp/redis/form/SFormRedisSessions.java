    /**
     * Metodo para configurar y poner los valores en las tablas. 
     * Configura las columnas, el ancho y si son editables. 
     * Pone los valores obtenidos de las keys en Redis de las sesiones
     * de usuario y los candados.
     */
    private void setTables(){
        DefaultTableModel modeloTableSessions = new DefaultTableModel();
        DefaultTableModel modeloTableAllLocks = new DefaultTableModel();
        
        jTSessions.setModel(modeloTableSessions);
        
        modeloTableSessions.addColumn("Type");
        modeloTableSessions.addColumn("session Id");
        modeloTableSessions.addColumn("company Id");
        modeloTableSessions.addColumn("User Id");
        modeloTableSessions.addColumn("User Name");

        int[] anchosSessions = {50, 50, 50, 50, 50};
        for (int i = 0; i < jTSessions.getColumnCount(); i++) {
            jTSessions.getColumnModel().getColumn(i).setPreferredWidth(anchosSessions[i]);
            jTSessions.getColumnModel().getClass();
            jTSessions.setDefaultEditor(Object.class, null);
        }
        
        Vector sessionsUsers = SRedisConnectionUtils.getSessionsUsers(jedis);
        
        for(int i=0; i<sessionsUsers.size(); i++){
            String stringKey = sessionsUsers.get(i).toString();
            String[] splitKey = stringKey.split("\\+");
            modeloTableSessions.addRow(splitKey);
        }
        
        jTLocks.setModel(modeloTableAllLocks);
        
        modeloTableAllLocks.addColumn("Type");
        modeloTableAllLocks.addColumn("id");
        modeloTableAllLocks.addColumn("companyId");
        modeloTableAllLocks.addColumn("RegistryType");
        modeloTableAllLocks.addColumn("pk");
        modeloTableAllLocks.addColumn("sessionId");
        modeloTableAllLocks.addColumn("userId");
        
        int[] anchosAllLocks = {70, 70, 100, 100, 70, 70, 70};
        for (int i = 0; i < jTLocks.getColumnCount(); i++) {
            jTLocks.getColumnModel().getColumn(i).setPreferredWidth(anchosAllLocks[i]);
            jTLocks.getColumnModel().getClass();
            jTLocks.setDefaultEditor(Object.class, null);
        }
        
        Vector keys = SRedisLockUtils.getLocksList(jedis);
                
        for(int i=0; i<keys.size(); i++){
            String stringKey = keys.get(i).toString();
            String[] splitKey = stringKey.split("\\+");
            modeloTableAllLocks.addRow(splitKey);
        } 
    }
    
    /**
     * Metodo para eliminar un candado de acceso exclusivo. 
     * Toma los parametros del candado seleccionado para eliminarlo del servidor Redis.
     */
    private void actionEraseLock(){
        String keyLock = jtType1.getText() + "+" +
                        jtLockId.getText() + "+" +
                        jtLockCompanyId.getText() + "+" +
                        jtRegistryType.getText() + "+" +
                        jtPK.getText() + "+" +
                        jtLockSessionId.getText() + "+" +
                        jtLockUserId.getText();
        
        if(jtType1.getText().equals("Lock") && !jtLockId.getText().equals("") && !jtLockCompanyId.getText().equals("") && 
                !jtRegistryType.getText().equals("") && !jtPK.getText().equals("") && !jtLockSessionId.getText().equals("") && 
                !jtLockUserId.getText().equals("")){
            int option = JOptionPane.showConfirmDialog(this, "Seguro que desea eliminar el candado:\n" + keyLock);
            if(option == 0){
                if(jedis.del(keyLock) == 1){
                    refreshTable();
                }else{
                    JOptionPane.showMessageDialog(this, "No se encontro el candado a borrar");
                    refreshTable();
                }
            }
        }else{
            JOptionPane.showMessageDialog(this, "No se selecciono ningun candado");
        }
            
    }
    
    /**
     * Metodo para eliminar una sesion del servidor Redis. 
     * Toma los parametros de la sesion seleccionada para eliminarlo del servidor Redis.
     * NOTA: De momento el metodo solo elimina la sesion mas no bloquea al usuario, el usuario puede volver
     *  a conectarse al servidor Redis.
     */
    private void actionEraseSession() {
        String keySession = jtType.getText() + "+" +
                        jtSessionId.getText() + "+" +
                        jtCompanyId.getText() + "+" +
                        jtUserId.getText() + "+" +
                        jtUserName.getText();
        
        if (jtType.getText().equals(SRedisConnectionUtils.SESSION) && !jtSessionId.getText().equals("") && !jtCompanyId.getText().equals("") &&
                !jtUserId.getText().equals("") && !jtUserName.getText().equals("")) {
            
            int option;
            if (!jedis.clientId().toString().equals(jtSessionId.getText())) {
                option = JOptionPane.showConfirmDialog(this, "Seguro que desea eliminar la sesión:\n\n" + keySession + "\n");
            }else{
                JOptionPane.showMessageDialog(this, "La sesión:\n\n" + keySession
                        + "\n\nEs su sesión de usuario");
                option = 1;
            }
            if(option == 0){
                jedis.del(keySession);
                Long killSession = jedis.clientKill(new ClientKillParams().id(jtSessionId.getText()));
                
                if (killSession == 1) {
                    JOptionPane.showMessageDialog(this, "Se eliminó la sesión\n" + keySession);
                } else {
                    JOptionPane.showMessageDialog(this, "ocurrió un error al eliminar la sesión\n" + keySession);
                }
                refreshTable();
            }
        } else {
            JOptionPane.showMessageDialog(this, "No se selecciono ninguna sesión");
        }
    }
    
