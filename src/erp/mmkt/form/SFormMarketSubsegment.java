    @Override
    public void setRegistry(erp.lib.data.SDataRegistry registry) {
        moMarketSubsegment = (SDataMarketSubSegment) registry;

        moFieldPkMarketSegmentId.setFieldValue(new int[] { moMarketSubsegment.getPkMarketSegmentId() });
        moFieldMarketSubsegment.setFieldValue(moMarketSubsegment.getMarketSubsegment());
        moFieldIsDeleted.setFieldValue(moMarketSubsegment.getIsDeleted());

        if (moMarketSubsegment.getIsCanDelete()) {
            jckIsDeleted.setEnabled(false);
        } else {
            jckIsDeleted.setEnabled(true);
        }

        jcbPkMarketSegmentId.setEnabled(false);
    }

    @Override
    public erp.lib.data.SDataRegistry getRegistry() {
        if (moMarketSubsegment == null) {
            moMarketSubsegment = new SDataMarketSubSegment();

            moMarketSubsegment.setPkMarketSegmentId(moFieldPkMarketSegmentId.getKeyAsIntArray()[0]);
            moMarketSubsegment.setFkUserNewId(miClient.getSession().getUser().getPkUserId());
        }
        else {
            moMarketSubsegment.setFkUserEditId(miClient.getSession().getUser().getPkUserId());
        }

        moMarketSubsegment.setMarketSubsegment(moFieldMarketSubsegment.getString());
        moMarketSubsegment.setIsCanEdit(true);
        if (!moMarketSubsegment.getIsCanDelete())
            moMarketSubsegment.setIsCanDelete(false);
        else
            moMarketSubsegment.setIsCanDelete(true);

        moMarketSubsegment.setIsDeleted(moFieldIsDeleted.getBoolean());

        return moMarketSubsegment;
    }

