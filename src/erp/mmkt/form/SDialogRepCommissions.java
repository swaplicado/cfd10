    private void actionPrint() {
        Cursor cursor = getCursor();
        SFormValidation validation = formValidate();
        Map<String, Object> oMap = null;
        JasperPrint jasperPrint = null;
        JasperViewer jasperViewer = null;

        if (validation.getIsError()) {
            if (validation.getComponent() != null) {
                validation.getComponent().requestFocus();
            }
            if (validation.getMessage().length() > 0) {
                miClient.showMsgBoxWarning(validation.getMessage());
            }
        }
        else {
            try {
                oMap = miClient.createReportParams();

                oMap.put("tDateStart", moFieldDateInitial.getDate());
                oMap.put("tDateEnd", moFieldDateEnd.getDate());
                oMap.put("sSqlWherePeriodCom", " AND c.dt_comms BETWEEN '" + SLibUtils.DbmsDateFormatDate.format(moFieldDateInitial.getDate()) + "' AND '" + SLibUtils.DbmsDateFormatDate.format(moFieldDateEnd.getDate()) + "' ");
                oMap.put("nFidCtDps", SDataConstantsSys.TRNS_CT_DPS_SAL);
                oMap.put("nFidClDps", SDataConstantsSys.TRNU_TP_DPS_SAL_INV[1]);
                oMap.put("nFidTpDps", SDataConstantsSys.TRNU_TP_DPS_SAL_INV[2]);
                oMap.put("nFidStDps", SDataConstantsSys.TRNS_ST_DPS_EMITED);
                oMap.put("nFidValDpsId", SDataConstantsSys.TRNS_ST_DPS_VAL_EFF);
                oMap.put("nFidCtBpId", SDataConstantsSys.BPSS_CT_BP_CUS);
                oMap.put("nSalesAgentId", moFieldFkSalesAgentId_n.getKeyAsIntArray()[0]);
                oMap.put("sSalesAgent", jcbFkSalesAgentId_n.getSelectedItem().toString());
                oMap.put("sCompanyBranch", moFieldFkCompanyBranchId.getKeyAsIntArray()[0] == 0 ? "(TODAS)" : jcbFkCompanyBranchId.getSelectedItem().toString());
                oMap.put("sSqlWhereCompanyBranch", moFieldFkCompanyBranchId.getKeyAsIntArray()[0] == 0 ? "" : " AND d.fid_cob = " + moFieldFkCompanyBranchId.getKeyAsIntArray()[0] + " ");
                oMap.put("sSqlHaving", jckCommissionsPayed.isSelected() ? "HAVING f_pay > 0 " : "");
                oMap.put("sSqlInnerLeftPayment", " LEFT OUTER JOIN mkt_comms_pay AS p ON p.fk_sal_agt = c.id_sal_agt AND p.b_del = 0 " +
                        "LEFT OUTER JOIN mkt_comms_pay_ety AS pe ON p.id_pay = pe.id_pay AND pe.fk_year = d.id_year " +
                        "AND pe.fk_doc = d.id_doc AND pe.fk_ety = de.id_ety AND pe.fk_sal_agt = c.id_sal_agt ");
                oMap.put("sSqlWherePayment", "");

                jasperPrint = SDataUtilities.fillReport(miClient, mnReportType, oMap);
                jasperViewer = new JasperViewer(jasperPrint, false);
                jasperViewer.setTitle(jckCommissionsPayed.isSelected() ? "Reporte de comisiones pagadas" : "Reporte de comisiones y pagos");
                jasperViewer.setVisible(true);
            }
            catch(Exception e) {
                SLibUtilities.renderException(this, e);
            }
            finally {
                setCursor(cursor);
            }
        }
    }

