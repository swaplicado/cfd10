    @Override
    public void setRegistry(erp.lib.data.SDataRegistry registry) {
        int i = 0;
        moGangEntry = (SDataGangEntry) registry;

        moFieldFkBizPartnerId.setFieldValue(new int[] { moGangEntry.getFkBizPartnerId() });
        moFieldIsDeleted.setFieldValue(moGangEntry.getIsDeleted());

        jckIsDeleted.setEnabled(true);
    }

    @Override
    public erp.lib.data.SDataRegistry getRegistry() {
        SDataBizPartner oBizPartner = null;

        if (moGangEntry == null) {
            moGangEntry = new SDataGangEntry();

            moGangEntry.setFkUserNewId(miClient.getSession().getUser().getPkUserId());
            moGangEntry.setDbmsUserNew(miClient.getSessionXXX().getUser().getUser());
            moGangEntry.setDbmsUserEdit("(n/a)");
            moGangEntry.setDbmsUserDelete("(n/a)");
            moGangEntry.setUserNewTs(miClient.getSessionXXX().getSystemDate());
            moGangEntry.setUserEditTs(miClient.getSessionXXX().getSystemDate());
            moGangEntry.setUserDeleteTs(miClient.getSessionXXX().getSystemDate());
        }
        else {
            moGangEntry.setDbmsUserEdit(miClient.getSessionXXX().getUser().getUser());
            moGangEntry.setFkUserEditId(miClient.getSession().getUser().getPkUserId());
            moGangEntry.setUserEditTs(miClient.getSessionXXX().getSystemDate());

            if (moFieldIsDeleted.getBoolean()) {
                moGangEntry.setFkUserDeleteId(miClient.getSession().getUser().getPkUserId());
                moGangEntry.setDbmsUserDelete(miClient.getSessionXXX().getUser().getUser());
                moGangEntry.setUserDeleteTs(miClient.getSessionXXX().getWorkingDate());
            }
        }

        oBizPartner = (SDataBizPartner) SDataUtilities.readRegistry(miClient, SDataConstants.BPSU_BP, moFieldFkBizPartnerId.getKeyAsIntArray(), SLibConstants.EXEC_MODE_VERBOSE);

        if (oBizPartner != null) {
            moGangEntry.setDbmsBizPartnerKey("");
        }

        moGangEntry.setDbmsBizPartner(jcbFkBizPartnerId.getSelectedItem().toString());
        moGangEntry.setFkBizPartnerId(moFieldFkBizPartnerId.getKeyAsIntArray()[0]);
        moGangEntry.setIsDeleted(moFieldIsDeleted.getBoolean());

        return moGangEntry;
    }

