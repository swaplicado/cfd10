    private void actionPrint() {
        Cursor cursor = getCursor();
        Map<String, Object> map = null;
        JasperPrint jasperPrint = null;
        JasperViewer jasperViewer = null;
        SFormValidation validation = formValidate();
        String titleReport = "";
        String sqlGroupBy = "";
        String sqlOrderBy = "";

        if (validation.getIsError()) {
            if (validation.getComponent() != null) {
                validation.getComponent().requestFocus();
            }
            if (validation.getMessage().length() > 0) {
                miClient.showMsgBoxWarning(validation.getMessage());
            }
        }
        else {
            try {
                setCursor(new Cursor(Cursor.WAIT_CURSOR));

                map = miClient.createReportParams();
                map.put("tDtInitial", moFieldDateInitial.getDate());
                map.put("tDtEnd", moFieldDateEnd.getDate());
                map.put("sDtInitial", miClient.getSessionXXX().getFormatters().getDbmsDateFormat().format(moFieldDateInitial.getDate()));
                map.put("sDtEnd", miClient.getSessionXXX().getFormatters().getDbmsDateFormat().format(moFieldDateEnd.getDate()));
                map.put("sCurCode", SDataReadDescriptions.getCatalogueDescription(miClient, SDataConstants.CFGU_CUR, new int[] {miClient.getSessionXXX().getParamsErp().getFkCurrencyId() }, SLibConstants.DESCRIPTION_CODE));
                map.put("Ct_In_Mfg_Rm_Asd", SDataConstantsSys.TRNS_TP_IOG_IN_MFG_RM_ASD[0]);
                map.put("Cl_In_Mfg_Rm_Asd", SDataConstantsSys.TRNS_TP_IOG_IN_MFG_RM_ASD[1]);
                map.put("Tp_In_Mfg_Rm_Asd", SDataConstantsSys.TRNS_TP_IOG_IN_MFG_RM_ASD[2]);
                map.put("Ct_Out_Mfg_Rm_Ret", SDataConstantsSys.TRNS_TP_IOG_OUT_MFG_RM_RET[0]);
                map.put("Cl_Out_Mfg_Rm_Ret", SDataConstantsSys.TRNS_TP_IOG_OUT_MFG_RM_RET[1]);
                map.put("Tp_Out_Mfg_Rm_Ret", SDataConstantsSys.TRNS_TP_IOG_OUT_MFG_RM_RET[2]);
                map.put("Ct_In_Mfg_Wp_Asd", SDataConstantsSys.TRNS_TP_IOG_IN_MFG_WP_ASD[0]);
                map.put("Cl_In_Mfg_Wp_Asd", SDataConstantsSys.TRNS_TP_IOG_IN_MFG_WP_ASD[1]);
                map.put("Tp_In_Mfg_Wp_Asd", SDataConstantsSys.TRNS_TP_IOG_IN_MFG_WP_ASD[2]);
                map.put("Ct_Out_Mfg_Wp_Ret", SDataConstantsSys.TRNS_TP_IOG_OUT_MFG_WP_RET[0]);
                map.put("Cl_Out_Mfg_Wp_Ret", SDataConstantsSys.TRNS_TP_IOG_OUT_MFG_WP_RET[1]);
                map.put("Tp_Out_Mfg_Wp_Ret", SDataConstantsSys.TRNS_TP_IOG_OUT_MFG_WP_RET[2]);
                map.put("Ct_Out_Mfg_Wp_Asd", SDataConstantsSys.TRNS_TP_IOG_OUT_MFG_WP_ASD[0]);
                map.put("Cl_Out_Mfg_Wp_Asd", SDataConstantsSys.TRNS_TP_IOG_OUT_MFG_WP_ASD[1]);
                map.put("Tp_Out_Mfg_Wp_Asd", SDataConstantsSys.TRNS_TP_IOG_OUT_MFG_WP_ASD[2]);
                map.put("Ct_In_Mfg_Wp_Ret", SDataConstantsSys.TRNS_TP_IOG_IN_MFG_WP_RET[0]);
                map.put("Cl_In_Mfg_Wp_Ret", SDataConstantsSys.TRNS_TP_IOG_IN_MFG_WP_RET[1]);
                map.put("Tp_In_Mfg_Wp_Ret", SDataConstantsSys.TRNS_TP_IOG_IN_MFG_WP_RET[2]);
                map.put("Ct_Out_Mfg_Fg_Asd", SDataConstantsSys.TRNS_TP_IOG_OUT_MFG_FG_ASD[0]);
                map.put("Cl_Out_Mfg_Fg_Asd", SDataConstantsSys.TRNS_TP_IOG_OUT_MFG_FG_ASD[1]);
                map.put("Tp_Out_Mfg_Fg_Asd", SDataConstantsSys.TRNS_TP_IOG_OUT_MFG_FG_ASD[2]);
                map.put("Ct_In_Mfg_Fg_Ret", SDataConstantsSys.TRNS_TP_IOG_IN_MFG_FG_RET[0]);
                map.put("Cl_In_Mfg_Fg_Ret", SDataConstantsSys.TRNS_TP_IOG_IN_MFG_FG_RET[1]);
                map.put("Tp_In_Mfg_Fg_Ret", SDataConstantsSys.TRNS_TP_IOG_IN_MFG_FG_RET[2]);
                
                switch (mnFormType) {
                    case SDataConstantsSys.REP_MFG_FINISHED_GOODS_EFFICIENCY:
                    case SDataConstantsSys.REP_MFG_RAW_MATERIALS_EFFICIENCY:
                        map.put("Ct_In_Mfg_Fg_Asd", SDataConstantsSys.TRNS_TP_IOG_IN_MFG_FG_ASD[0]);
                        map.put("Cl_In_Mfg_Fg_Asd", SDataConstantsSys.TRNS_TP_IOG_IN_MFG_FG_ASD[1]);
                        map.put("Tp_In_Mfg_Fg_Asd", SDataConstantsSys.TRNS_TP_IOG_IN_MFG_FG_ASD[2]);
                        map.put("Ct_Out_Mfg_Fg_Ret", SDataConstantsSys.TRNS_TP_IOG_OUT_MFG_FG_RET[0]);
                        map.put("Cl_Out_Mfg_Fg_Ret", SDataConstantsSys.TRNS_TP_IOG_OUT_MFG_FG_RET[1]);
                        map.put("Tp_Out_Mfg_Fg_Ret", SDataConstantsSys.TRNS_TP_IOG_OUT_MFG_FG_RET[2]);
                        map.put("Rep_Finished_Goods_Efficiency", SDataConstantsSys.REP_MFG_FINISHED_GOODS_EFFICIENCY);
                        map.put("Rep_Raw_Materials_Efficiency", SDataConstantsSys.REP_MFG_RAW_MATERIALS_EFFICIENCY);
                        map.put("nReport", mnFormType);
                        
                        if (mnFormType == SDataConstantsSys.REP_MFG_FINISHED_GOODS_EFFICIENCY) {
                            sqlGroupBy = "GROUP BY o.fid_bom, i.id_item ";
                            sqlOrderBy = "ORDER BY o.id_year, o.id_ord DESC, o.num, o.ref, o.fid_bom, st.st, o.dt ";
                            map.put("sTitle", "EFICIENCIA GLOBAL DE PRODUCTOS TERMINADOS");
                            titleReport = "Eficiencia global de productos terminados";
                        }
                        else {
                            sqlGroupBy = "GROUP BY i.id_item ";
                            sqlOrderBy = "ORDER BY i.item_key, i.id_item, o.id_year, o.id_ord, o.num ";
                            map.put("sTitle", "EFICIENCIA GLOBAL DE MATERIAS PRIMAS");
                            titleReport = "Eficiencia global de insumos";
                        }
                        
                        map.put("sGroupBy", sqlGroupBy);
                        map.put("sOrderBy", sqlOrderBy);
                        break;
                    case SDataConstantsSys.REP_MFG_ORD_PERFORMANCE:
                        titleReport = "Rendimiento de orden de producción";
                        break;
                }

                jasperPrint = SDataUtilities.fillReport(miClient, mnFormType, map);
                jasperViewer = new JasperViewer(jasperPrint, false);
                jasperViewer.setTitle(titleReport);
                jasperViewer.setVisible(true);
            }
            catch(Exception e) {
                SLibUtilities.renderException(this, e);
            }
            finally {
                setCursor(cursor);
            }
        }
    }

    private void actionDateInitial() {
        miClient.getGuiDatePickerXXX().formReset();
        miClient.getGuiDatePickerXXX().setDate(moFieldDateInitial.getDate());
        miClient.getGuiDatePickerXXX().setVisible(true);

        if (miClient.getGuiDatePickerXXX().getFormResult() == SLibConstants.FORM_RESULT_OK) {
            moFieldDateInitial.setFieldValue(miClient.getGuiDatePickerXXX().getGuiDate());
            jftDateInitial.requestFocus();
        }
    }

    private void actionDateEnd() {
        miClient.getGuiDatePickerXXX().formReset();
        miClient.getGuiDatePickerXXX().setDate(moFieldDateEnd.getDate());
        miClient.getGuiDatePickerXXX().setVisible(true);

        if (miClient.getGuiDatePickerXXX().getFormResult() == SLibConstants.FORM_RESULT_OK) {
            moFieldDateEnd.setFieldValue(miClient.getGuiDatePickerXXX().getGuiDate());
            jftDateEnd.requestFocus();
        }
    }

    private void actionClose() {
        mnFormResult = SLibConstants.FORM_RESULT_CANCEL;
        setVisible(false);
    }

