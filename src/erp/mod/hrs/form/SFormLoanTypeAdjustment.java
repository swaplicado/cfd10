    @Override
    public void reloadCatalogues() {
        miClient.getSession().populateCatalogue(moKeyLoanType, SModConsts.HRSS_TP_LOAN, SModConsts.HRS_LOAN, null);
    }

    @Override
    public void setRegistry(SDbRegistry registry) throws Exception {
        int loanType = SLibConsts.UNDEFINED;
        moRegistry = (SDbLoanTypeAdjustment) registry;

        mnFormResult = SLibConsts.UNDEFINED;
        mbFirstActivation = true;

        removeAllListeners();
        reloadCatalogues();

        loanType = moRegistry.getPkLoanTypeId();

        if (moRegistry.isRegistryNew()) {
            moRegistry.initPrimaryKey();
            moRegistry.setDateStart(miClient.getSession().getCurrentDate());
            jtfRegistryKey.setText("");
        }
        else {
            jtfRegistryKey.setText(SLibUtils.textKey(moRegistry.getPrimaryKey()));
        }

        moKeyLoanType.setValue(new int[] { loanType });
        moDateDateStart.setValue(moRegistry.getDateStart());
        moCurAdjustment.getField().setValue(moRegistry.getAdjustment());

        setFormEditable(true);

        if (moRegistry.isRegistryNew()) {
            moKeyLoanType.setEnabled(true);
        }
        else {
            moKeyLoanType.setEnabled(false);
        }

        addAllListeners();
    }

    @Override
    public SDbRegistry getRegistry() throws Exception {
        SDbLoanTypeAdjustment registry = moRegistry.clone();

        if (registry.isRegistryNew()) {
            registry.setPkLoanTypeId(moKeyLoanType.getValue()[0]);
        }

        registry.setDateStart(moDateDateStart.getValue());
        registry.setAdjustment(moCurAdjustment.getField().getValue());

        return registry;
    }

