package erp.mod.hrs.utils;

/**
 *
 * @author Edwin Carmona
 */
public class SEarnConfiguration {
    private int idEarning;
    private int idBonus;
    private double amount;
    private double hasWon;
    private String comments;
    
    private String numEmployee;
    private String employee;
    private int checkerPolicy;
    private String bonus;
    private String earning;
    
    public int getIdEarning() {
        return idEarning;
    }

    public void setIdEarning(int idEarning) {
        this.idEarning = idEarning;
    }

    public int getIdBonus() {
        return idBonus;
    }

    public void setIdBonus(int idBonus) {
        this.idBonus = idBonus;
    }

    public double getAmount() {
        return amount;
    }

    public void setAmount(double amount) {
        this.amount = amount;
    }

    public double isHasWon() {
        return hasWon;
    }

    public void setHasWon(double hasWon) {
        this.hasWon = hasWon;
    }

    public String getComments() {
        return comments;
    }

    public void setComments(String comments) {
        this.comments = comments;
    }

    public String getNumEmployee() {
        return numEmployee;
    }

    public void setNumEmployee(String numEmployee) {
        this.numEmployee = numEmployee;
    }

    public String getEmployee() {
        return employee;
    }

    public void setEmployee(String employee) {
        this.employee = employee;
    }

    public int getCheckerPolicy() {
        return checkerPolicy;
    }

    public void setCheckerPolicy(int checkerPolicy) {
        this.checkerPolicy = checkerPolicy;
    }

    public String getBonus() {
        return bonus;
    }

    public void setBonus(String bonus) {
        this.bonus = bonus;
    }

    public String getEarning() {
        return earning;
    }

    public void setEarning(String earning) {
        this.earning = earning;
    }
    
    
    
}
