    /**
     * @param iogTypeKey IOG type key. Constants defined in SDataConstantsSys.
     * @param warehouseMode Warehouse mode (source or destiny). Constants defined in SLibConstants (i.e. MODE_AS_SRC, MODE_AS_DES).
     */
    public void setIogSettings(final int[] iogTypekey, final int warehouseMode) {
        mnWarehouseMode = warehouseMode;
        manIogTypeKey = iogTypekey;

        if (manIogTypeKey == null) {
            manIogClassKey = null;
            jtfSettings.setText("N/A");
        }
        else {
            manIogClassKey = new int[] { manIogTypeKey[0], manIogTypeKey[1] };
            jtfSettings.setText(SDataReadDescriptions.getCatalogueDescription(miClient, SDataConstants.TRNS_TP_IOG, manIogTypeKey));
        }

        switch (warehouseMode) {
            case SLibConstants.MODE_AS_SRC:
                jtfSettingsEntity.setText("ALMACÉN ORIGEN");
                break;
            case SLibConstants.MODE_AS_DES:
                jtfSettingsEntity.setText("ALMACÉN DESTINO");
                break;
            default:
                miClient.showMsgBoxWarning(SLibConstants.MSG_ERR_UTIL_UNKNOWN_OPTION);
        }

        jtfSettings.setCaretPosition(0);
        jtfSettingsEntity.setCaretPosition(0);
    }

    public void setCompanyBranchKey(int[] key) {
        mnCompanyBranchId = SLibConstants.UNDEFINED;
        mnEntityId = SLibConstants.UNDEFINED;

        if (key == null) {
            moFieldCompanyBranchId.setKey(null);
        }
        else {
            mnCompanyBranchId = key[0];
            moFieldCompanyBranchId.setKey(getCompanyBranchKey());
        }

        itemStateChangedCompanyBranch();
    }

    public void setCompanyBranchEntityKey(int[] key) {
        mnCompanyBranchId = SLibConstants.UNDEFINED;
        mnEntityId = SLibConstants.UNDEFINED;

        if (key == null) {
            moFieldCompanyBranchId.setKey(null);
            itemStateChangedCompanyBranch();
        }
        else {
            mnCompanyBranchId = key[0];
            mnEntityId = key[1];

            moFieldCompanyBranchId.setKey(getCompanyBranchKey());
            itemStateChangedCompanyBranch();

            moFieldEntity.setKey(getCompanyBranchEntityKey());
        }
    }

    public int[] getCompanyBranchKey() {
        return new int[] { mnCompanyBranchId };
    }

    public int[] getCompanyBranchEntityKey() {
        return new int[] { mnCompanyBranchId, mnEntityId };
    }

