    private void windowActivated() {
        if (mbFirstTime) {
            mbFirstTime = false;

            if (jltEntity.isEnabled()) {
                jltEntity.requestFocus();
            }
            else if (jcbCompanyBranch.isEnabled()) {
                jcbCompanyBranch.requestFocus();
            }
            else {
                jbOk.requestFocus();
            }
        }
    }

    private boolean isUniversalCompany() {
        return mbIsUniversalCompany;
    }

    private boolean isUniversalCompanyBranch() {
        return isUniversalCompany() || miClient.getSessionXXX().getIsUniversalCompanyBranch(moFieldCompanyBranchId.getKeyAsIntArray()[0]) || miClient.getSessionXXX().getIsUniversalCompanyBranchEntities(moFieldCompanyBranchId.getKeyAsIntArray()[0], mnEntityCategoryId);
    }

    private void itemStateChangedCompanyBranch() {
        int[][] warehouseTypeKeys = null;
        SDataCompanyBranchEntity entity = null;

        if (jcbCompanyBranch.getSelectedIndex() <= 0) {
            mvItemsEntitiesAll = new Vector<SFormComponentItem>();
        }
        else {
            if (isUniversalCompanyBranch()) {
                mvItemsEntitiesAll = miClient.getSessionXXX().getAllCompanyBranchEntitiesAsComponentItems(moFieldCompanyBranchId.getKeyAsIntArray()[0], mnEntityCategoryId, false);
            }
            else {
                mvItemsEntitiesAll = miClient.getSessionXXX().getUserCompanyBranchEntitiesAsComponentItems(moFieldCompanyBranchId.getKeyAsIntArray()[0], mnEntityCategoryId, false);
            }

            if (manIogTypeKey != null) {
                warehouseTypeKeys = STrnUtilities.getAppropriateWarehouseTypes(manIogTypeKey, mnWarehouseMode);
            }
        }

        mvItemsEntities = new Vector<SFormComponentItem>();

        if (mvItemsEntitiesAll.size() > 0) {
            if (warehouseTypeKeys == null) {
                mvItemsEntities.addAll(mvItemsEntitiesAll);
            }
            else {
                for (SFormComponentItem item : mvItemsEntitiesAll) {
                    entity = (SDataCompanyBranchEntity) SDataUtilities.readRegistry(miClient, SDataConstants.CFGU_COB_ENT, item.getPrimaryKey(), SLibConstants.EXEC_MODE_VERBOSE);
                    if (SLibUtilities.belongsTo(new int[] { entity.getFkEntityCategoryId(), entity.getFkEntityTypeId() }, warehouseTypeKeys)) {
                        mvItemsEntities.add(item);
                    }
                }
            }
        }

        jltEntity.setListData(mvItemsEntities);
        jltEntity.setEnabled(mvItemsEntities.size() > 0);

        if (jltEntity.isEnabled()) {
            jltEntity.setSelectedIndex(0);
        }

        jbViewAll.setEnabled(mvItemsEntities.size() < mvItemsEntitiesAll.size());
    }

    public void actionViewAll() {
        int[] key = moFieldEntity.getKeyAsIntArray();

        jltEntity.setListData(mvItemsEntitiesAll);
        jltEntity.setEnabled(mvItemsEntitiesAll.size() > 0);

        if (jltEntity.isEnabled()) {
            moFieldEntity.setKey(key);
        }

        jbViewAll.setEnabled(false);
        jltEntity.requestFocus();
    }

    public void actionOk() {
        if (jcbCompanyBranch.getSelectedIndex() <= 0) {
            miClient.showMsgBoxWarning(SLibConstants.MSG_ERR_GUI_FIELD_EMPTY + "'" + jlCompanyBranch.getText() + "'.");
            jcbCompanyBranch.requestFocus();
        }
        else if (jltEntity.getSelectedIndex() == -1) {
            miClient.showMsgBoxWarning(SLibConstants.MSG_ERR_GUI_FIELD_EMPTY + "'" + jlEntity.getText() + "'.");
            jltEntity.requestFocus();
        }
        else {
            mnCompanyBranchId = moFieldEntity.getKeyAsIntArray()[0];
            mnEntityId = moFieldEntity.getKeyAsIntArray()[1];

            mnFormResult = SLibConstants.FORM_RESULT_OK;
            setVisible(false);
        }
    }

    public void actionCancel() {
        mnFormResult = SLibConstants.FORM_RESULT_CANCEL;
        setVisible(false);
    }

