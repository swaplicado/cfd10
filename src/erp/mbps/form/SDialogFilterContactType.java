    public void setContactType(int type) {
        mbResetingForm = true;

        mnContactType = type;
        jckSelectAll.setSelected(mnContactType == SLibConstants.UNDEFINED);
        moFieldContactType.setKey(mnContactType == SLibConstants.UNDEFINED ? null : new int[] { mnContactType });
        itemStateChangedSelectAll();

        mbResetingForm = false;
    }

