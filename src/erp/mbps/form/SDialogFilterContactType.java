    private void initComponentsExtra() {
        moFieldContactType = new SFormField(miClient, SLibConstants.DATA_TYPE_KEY, true, jltContactType, jlContactType);

        mvFields = new Vector<SFormField>();
        mvFields.add(moFieldContactType);

        jbOk.addActionListener(this);
        jbCancel.addActionListener(this);

        SFormUtilities.createActionMap(rootPane, this, "actionOk", "ok", KeyEvent.VK_ENTER, KeyEvent.CTRL_DOWN_MASK);
        SFormUtilities.createActionMap(rootPane, this, "actionCancel", "cancel", KeyEvent.VK_ESCAPE, SLibConstants.UNDEFINED);
    }

    private void windowActivated() {
        if (mbFirstTime) {
            mbFirstTime = false;

            if (jltContactType.isEnabled()) {
                jltContactType.requestFocus();
            }
            else {
                jckSelectAll.requestFocus();
            }
        }
    }

    private void itemStateChangedSelectAll() {
        if (jckSelectAll.isSelected()) {
            jltContactType.setEnabled(false);
            moFieldContactType.resetField();
        }
        else {
            jltContactType.setEnabled(true);
        }
    }

    public void actionOk() {
        if (!jckSelectAll.isSelected() && jltContactType.getSelectedIndex() < 0) {
            jltContactType.requestFocus();
            miClient.showMsgBoxWarning(SLibConstants.MSG_ERR_GUI_FIELD_EMPTY + "'" + jlContactType.getText() + "'.");
        }
        else {
            mnContactType = jckSelectAll.isSelected() ? SLibConstants.UNDEFINED : moFieldContactType.getKeyAsIntArray()[0];

            mnFormResult = SLibConstants.FORM_RESULT_OK;
            setVisible(false);
        }
    }

    public void actionCancel() {
        mnFormResult = SLibConstants.FORM_RESULT_CANCEL;
        setVisible(false);
    }

